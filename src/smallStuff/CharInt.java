package smallStuff;

public class CharInt
{
	private char character;
	private int integer;
	
	public CharInt(char character, int integer)
	{
		this.character = character;
		this.integer = integer;
	}

	public char getCharacter()
	{
		return character;
	}

	public int getInteger()
	{
		return integer;
	}
}
